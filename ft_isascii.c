#include "libft.h"

int	ft_isascii(int c)
{
	int	i;

	i = 0;
	while (i != '\0')
	{
		if ((c >= 0 && c <= 127) //Pedir ayuda para este
			|| (c >= 'A' && c <= 'Z'))
		i++;
		else
			return (0);
	}
	return (1);
}
