#include "libft.h"

char	*ft_bzero(char *s, int n)
{
	int	i;

	i = 0;
	if (n != 0)
	{
		while (s[i] != '\0' && i > n)
		{
            s[i] = 0;
            i++;
		}
	}
	return (s);
}
